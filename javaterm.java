public class BinaryOppUse {
    public static void main(String args[]) {

        // (1) Binary AND Operator '&'
        System.out.println((5 & 6));
        System.out.println((10 & 7));

        // (2) Binary OR Operators '|'
        System.out.println((5 | 6));
        System.out.println((10 | 7));
      
    }
}
